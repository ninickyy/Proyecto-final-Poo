    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String nombrePartido = txtNombrePartido.getText();
        String sigla = txtSigla.getText();
        String logo = txtLogo.getText();
        String representanteLegal = txtRepresentanteLegal.getText();
        
        gestionPartido.eliminar(nombrePartido, sigla, logo, representanteLegal);
        JOptionPane.showMessageDialog(rootPane, "Partido eliminado.");
    }//GEN-LAST:event_btnEliminarActionPerformed

