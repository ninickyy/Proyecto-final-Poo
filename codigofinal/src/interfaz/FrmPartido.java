    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombrePartido = txtNombrePartido.getText();
        String sigla = txtSigla.getText();
        String logo = txtLogo.getText();
        String representanteLegal = txtRepresentanteLegal.getText();
        
        PartidoPolitico partido = new PartidoPolitico(nombrePartido,sigla,logo,representanteLegal);
        
        gestionPartido.registrar(partido);
    }//GEN-LAST:event_btnAgregarActionPerformed

